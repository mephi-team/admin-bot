package team.mephi.adminbot.model.enums;

/**
 * Статус пользователя в системе (кандидат, студент и т.д.).
 *
 * Показывает текущее состояние пользователя,
 * например: активен, отчислён, заблокирован и т.п.
 */
public enum UserStatus {
    ACTIVE,     // Активный пользователь
    EXPELLED,   // Отчислён
    BLOCKED,    // Заблокирован
    INACTIVE,   // Неактивен
    PENDING     // Ожидает подтверждения / обработки
}