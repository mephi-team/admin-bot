package team.mephi.adminbot.model.enums;

/**
 * Статус диалога в системе.
 *
 * Показывает текущее состояние диалога:
 * - ACTIVE: активный диалог, в котором идёт общение
 * - CLOSED: диалог закрыт (завершён, но может быть открыт снова)
 * - ARCHIVED: диалог архивирован (скрыт из основного списка)
 * - BLOCKED: диалог заблокирован (например, из-за нарушения правил)
 */
public enum DialogStatus {
    ACTIVE,     // Активный диалог
    CLOSED,     // Закрыт
    ARCHIVED,   // Архивирован
    BLOCKED     // Заблокирован
}

