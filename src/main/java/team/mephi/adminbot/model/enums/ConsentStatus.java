package team.mephi.adminbot.model.enums;

/**
 * Статус согласия пользователя на обработку данных.
 *
 * <p>Возможные статусы:
 * <ul>
 *   <li>GRANTED - пользователь дал согласие</li>
 *   <li>REVOKED - пользователь отозвал согласие</li>
 *   <li>PENDING - согласие ожидает подтверждения</li>
 * </ul>
 */
public enum ConsentStatus {
    GRANTED, // Пользователь дал согласие
    REVOKED, // Пользователь отозвал согласие
    PENDING // Согласие ожидает подтверждения
}

